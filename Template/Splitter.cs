using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Template
{
    public static class Splitter
    {
        public static List<> (string s, char c)
        {
            string[] values = s.Split(c);

            List<> valuesList = new List<>();

            foreach (string value in values)
            {
                valuesList.Add();
            }


            return valuesList;
        }
    }
}
